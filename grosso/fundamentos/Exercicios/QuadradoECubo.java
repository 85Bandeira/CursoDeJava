package fundamentos.Exercicios;

public class QuadradoECubo {
	public static void main(String[] args) {
		
		double n = 8;
		double quadrado = n * n;
		double cubo = n * n * n;
		
		System.out.println("O número " + n + " elevado ao quadrado é " + quadrado );
		System.out.println("O número " + n + " elevado ao cubo é " + cubo );
	}

}
